int	ft_str_is_alpha(char *str)
{
	int	alph;
	int	i;

	alph = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] < 'A' || str[i] > 'Z')
			&& (str[i] < 'a' || str[i] > 'z'))
		{
			alph = 0;
		}
		i++;
	}
	return (alph);
}
